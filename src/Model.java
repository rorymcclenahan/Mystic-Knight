	private void checkPlayerCollision(GameObject player, GameObject enemy) {
	    if (Math.abs(enemy.getXDifference(enemy, player)) < player.getWidth() 
	            && Math.abs(enemy.getYDifference(enemy, player)) < player.getWidth()
	            && player.isAlive()) {
	        player.removeLife();
	        EnemiesList.remove(enemy);
	        System.out.println(player.getLives());
	        if (player.isDead()) {
	            enemy.startWalkIdle();
	            System.out.println("game over status");
	            System.out.println(MainWindow.gameOver);
	            MainWindow.gameOver = true;
	        }
	    }
	}


