	private void slashLogic() {
		for (GameObject Slash : SlashList) 
		{
			Slash.removeLife();
			if (Slash.isDead()) {
				SlashList.remove(Slash);
				continue;
			}
			GameObject Player = Slash.getParentObject(Slash);
			String direction = Slash.getDirection();
//			System.out.println(direction);
			float x = Player.getCentre().getX();
		    float y = Player.getCentre().getY();
//		    System.out.println("Player coords");
//		    System.out.println(x);
//		    System.out.println(y);
		    if (direction == "left") { // box to left of player
		    	x = Player.getCentre().getX() - 24;
		    	y = Player.getCentre().getY() - 8;
		    } else if (direction == "right") { // box to right of player
		    	x = Player.getCentre().getX() + Player.getWidth();
		    	y = Player.getCentre().getY() - 8;
		    } else if (direction == "down") { // box to down of player
		    	x = Player.getCentre().getX() - 8;
		    	y = Player.getCentre().getY() + Player.getHeight();
		    } else if (direction == "up") { // box to up of player
		    	x = Player.getCentre().getX() - 8;
		    	y = Player.getCentre().getY() - 24;
		    }
//		    System.out.println("Slash coords");
//		    System.out.println(x);
//		    System.out.println(y);
		    Slash.getCentre().setX(x);
		    Slash.getCentre().setY(y);
//			Slash.getCentre().ApplyVector(new Vector3f(Slash.getCentre().getX()-x,Slash.getCentre().getY()-y,0f)); // uncomment for funny slinging swords
		for (GameObject Enemy : EnemiesList) {
			
			if (Math.abs(Enemy.getCentre().getX()- Slash.getCentre().getX())< Slash.getWidth() 
				&& Math.abs(Enemy.getCentre().getY()- Slash.getCentre().getY()) < Slash.getHeight())
			{
				EnemiesList.remove(Enemy);
				SlashList.remove(Slash);
				Score++;
			}
			}
		}
	}

