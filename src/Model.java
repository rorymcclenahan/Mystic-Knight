	private void CreateSlash(GameObject Player, String direction) {
	    float x1, x2, y1, y2;
	    switch (direction) {
	        case "left":
	            x1 = Player.getCentre().getX() - 24;
	            x2 = Player.getCentre().getX();
	            y1 = Player.getCentre().getY() - 8;
	            y2 = Player.getCentre().getY() + Player.getHeight() + 8;
	            break;
	        case "right":
	            x1 = Player.getCentre().getX() + Player.getWidth();
	            x2 = Player.getCentre().getX() + Player.getWidth() + 24;
	            y1 = Player.getCentre().getY() - 8;
	            y2 = Player.getCentre().getY() + Player.getHeight() + 8;
	            break;
	        case "down":
	            x1 = Player.getCentre().getX() - 8;
	            x2 = Player.getCentre().getX() + Player.getWidth() + 8;
	            y1 = Player.getCentre().getY() + Player.getHeight();
	            y2 = Player.getCentre().getY() + Player.getHeight() + 24;
	            break;
	        case "up":
	        	x1 = Player.getCentre().getX() - 8;
	            x2 = Player.getCentre().getX() + Player.getWidth() + 8;
	            y1 = Player.getCentre().getY() - 24;
	            y2 = Player.getCentre().getY();
	            break;
	        default:
	            return; // invalid direction
	    }
		GameObject Slash = new GameObject("res/Slash.png",(int) (x2-x1),(int) (y2-y1),new Point3f(x1,y1,0));
		Slash.setLives(50); // reduce a life every call, so when lives hit 0, the slash "dies" and disappears
		switch (direction) {
        case "left":
        	Slash.startWalkLeft();
            break;
        case "right":
        	Slash.startWalkRight();
            break;
        case "up":
        	Slash.startWalkUp();
            break;
        case "down":
        	Slash.startWalkDown();
            break;
		}
		Slash.setParentObject(Player);
//		System.out.println(Slash.getDirection());
		
		SlashList.add(Slash);
	}
