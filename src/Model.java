	private void gameLogic() { 		
		// this is a way to increment across the array list data structure 
		
		if (Score >= 50 && CurrLevel == 1) {
			MainWindow.gameWin = true;
			
		}
		
		//see if they hit anything 
		// using enhanced for-loop style as it makes it alot easier both code wise and reading wise too 
		for (GameObject temp : EnemiesList) 
		{
		for (GameObject Bullet : BulletList) 
		{
			if ( Math.abs(temp.getCentre().getX()- Bullet.getCentre().getX())< temp.getWidth() 
				&& Math.abs(temp.getCentre().getY()- Bullet.getCentre().getY()) < temp.getHeight())
			{
				EnemiesList.remove(temp);
				BulletList.remove(Bullet);
				Score++;
			} 
		}
		}
		
	}

	private void enemyLogic() {
		// TODO Auto-generated method stub
		float speed = 1f;
		if (CurrLevel== 1) {
			speed = 1.5f;
		} 
		for (GameObject temp : EnemiesList) 
		{
		    // Move enemies to player
//			temp.getCentre().ApplyVector(new Vector3f(0,0,0));
			GameObject closestPlayer = MainWindow.twoPlayer ? temp.getClosestObject(temp, PlayersList) : Player1;
			if (closestPlayer != null && temp.getDistance(temp, closestPlayer) < 350f) {
			    temp.getCentre().ApplyVector(new Vector3f(
			            ((float) (temp.normalizeX(closestPlayer, temp)) * speed),
			            ((float) (temp.normalizeY(temp, closestPlayer)) * speed),
			            0));
			    
			    if (closestPlayer == Player1 && Player1.isAlive() ||
			        closestPlayer == Player2 && Player2.isAlive()) {
			        temp.updateEnemyDirection(temp, closestPlayer);
			    }
			} else {
			    temp.startWalkIdle();
			}
			 
			 
			// Check collision
			if (MainWindow.twoPlayer) {
				checkPlayerCollision(Player1, temp);
			    checkPlayerCollision(Player2, temp);
			} else {
			    checkPlayerCollision(Player1, temp);
			}
			
		}
		
		if (EnemiesList.size()<3)
		{
			while (EnemiesList.size()<6 && CurrLevel == 0)
			{
				EnemiesList.add(new GameObject("res/EnemySprite.png",32,32,new Point3f(700,((float)Math.random()*450 + 50),0))); 
			}
			while (EnemiesList.size()<8 && CurrLevel == 1)
			{
				EnemiesList.add(new GameObject("res/EnemySprite.png",50,50,new Point3f(700,((float)Math.random()*450 + 50),0))); 
			}
		}
	}

